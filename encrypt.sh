# +----- Variables ------------------------------------------------------------+
gnupg="/usr/bin/gpg"
algo="AES256"

# +----- Functions ------------------------------------------------------------+

# +----- Main -----------------------------------------------------------------+
if [[ "$#" = "0" ]]; then
    echo "No file specified."
    exit 1
fi
file=$1

${gnupg} --batch --passphrase-fd 0 --pinentry-mode loopback --symmetric --armor --cipher-algo ${algo} ${file}

# +----- End ------------------------------------------------------------------+
exit 0 
